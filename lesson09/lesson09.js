/**
 * ts中的泛型接口
 **/
var setData = function (params1, params2) {
    return params1 + params2;
};
setData('name', 'LeeFour');
var getData = function (value1, value2) {
    return value1 + "+" + value2;
};
getData('name', 'WongFive');
var getData2 = function (value1, value2) {
    return value1 + "+" + value2;
};
getData2('name', 'WongFive');
